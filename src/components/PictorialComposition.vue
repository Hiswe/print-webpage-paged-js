    <h1>Pictorial Composition</h1>
    <p>
      While too much science is often deadly to art, the true basis of pictorial composition is rigidly scientific, and
      all of the principles governing it are of use and importance to the printer, especially in planning displayed work
      and in title pages.
    </p>
    <p>
      Composition is that quality which gives a picture coherence, "the mortar of the wall." It was not esteemed of
      importance by the old masters, and many of their works do not show that they knew or cared for that which
      distinguishes a picture from a map, a group photograph, or a scientific diagram. It is the absence of composition,
      balance, unity, that makes ordinary photographs something other than true works of art. It is not primarily truth
      of representation that is necessary in a work of art, but truth of idealization; and that quality is beyond the
      conscious reach of the camera's lens. It is a redeeming and a justifying element added by the imagination of the
      artist. There may be a picture, by a photographer or by a painter, having all the requisite component parts to
      make it a work of art; there may be, for example, a woman, an axe, a road, a mountain, trees; but these thrown
      together upon a canvas do not make a work of art unless they are properly composed, even if they are arranged in
      an order satisfying to the realist, and each faultlessly executed. It is not the same thing to paint and to make
      pictures; to print and to execute artistic printing.
    </p>
    <p>
      The application of the rules of composition to pieces of printing made up in a whole or in part of "display"
      types is obviously essential to their beauty. It is the touch of beauty given to science that produces art. In
      printing the matter of securing balance and unity is at once more simple and more difficult than in painting. The
      component parts to be dealt with are more rigid and restricted, but are purely conventional and precise. The
      painter's conception is given balance and unity through the original drawing and color-scheme corrected and
      perfected by constant scrutiny and by tests and continual alterations. The printed piece must be balanced by a
      wise choice and skilful arrangement of the types, and a careful distribution of white space and black ink, or
      color. The actual center of a canvas is the center of attraction in a picture perfectly balanced. This does not
      mean that an equal amount of paint must be spread upon every quarter of the canvas, nor that objects of equal
      visual importance in themselves must be equally distributed over it. A tiny dot of distinctive paint, placed a
      certain distance from the center of the canvas, may perfectly balance an object ten times its size which is placed
      relatively nearer the center. Balance in printing must not be understood to mean that there must be an equal
      distribution of weight over all quarters of the piece, but that there must be a compensatory distribution of
      weight.
    </p>
